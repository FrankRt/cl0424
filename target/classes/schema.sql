DROP TABLE IF EXISTS TOOLS;
DROP TABLE IF EXISTS TOOL_TYPES;
DROP TABLE IF EXISTS HOLIDAYS;

CREATE TABLE HOLIDAYS (
    id INT AUTO_INCREMENT PRIMARY KEY,
    date DATE NOT NULL
);

CREATE TABLE TOOL_TYPES (
    tool_types_id INT AUTO_INCREMENT PRIMARY KEY,
    type VARCHAR(30) NOT NULL,
    daily_charge DOUBLE NOT NULL,
    weekday_charge BOOLEAN NOT NULL,
    weekend_charge BOOLEAN NOT NULL,
    holiday_charge BOOLEAN NOT NULL
);

CREATE TABLE TOOLS (
    id INT AUTO_INCREMENT PRIMARY KEY,
    tool_code VARCHAR(4) NOT NULL,
    tool_types_id INT,
    brand VARCHAR(20) NOT NULL,

    CONSTRAINT FK_TOOL_TYPES_ID
      FOREIGN KEY (tool_types_id)
      REFERENCES TOOL_TYPES(tool_types_id)
);